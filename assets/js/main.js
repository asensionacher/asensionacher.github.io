// Function to detect browser language
function getBrowserLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    const langCode = browserLang.toLowerCase().split('-')[0]; // Get just the language code (e.g., 'es' from 'es-ES')
    
    // Map browser language to our supported languages
    const supportedLangs = ['ca', 'es', 'en', 'ar', 'ur'];
    
    // If browser language is directly supported, use it
    if (supportedLangs.includes(langCode)) {
        return langCode;
    }
    
    // Default to Catalan
    return 'ca';
}

// Initialize language: use saved preference, or detect from browser, or default to Catalan
let currentLang = localStorage.getItem('preferredLanguage') || getBrowserLanguage();

window.setLanguage = function(lang) {
    currentLang = lang;
    localStorage.setItem('preferredLanguage', lang);
    
    // Update HTML lang and dir attributes
    const html = document.documentElement;
    html.setAttribute('lang', lang);
    html.setAttribute('dir', (lang === 'ar' || lang === 'ur') ? 'rtl' : 'ltr');
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = getTranslation(key, lang);
        if (translation) {
            element.textContent = translation;
        }
    });
    
    // Update language button
    updateLanguageButton(lang);
    
    // Update active state in dropdown
    updateLanguageDropdown(lang);
    
    // Call post translations update if available (for blog page)
    if (typeof window.updatePostTranslations === 'function') {
        window.updatePostTranslations(lang);
    }
    
    // Call blog previews update if available (for home page)
    if (typeof window.updateBlogPreviews === 'function') {
        window.updateBlogPreviews(lang);
    }
};

function getTranslation(key, lang) {
    const keys = key.split('.');
    let value = translations[lang];
    for (const k of keys) {
        if (value && value[k]) {
            value = value[k];
        } else {
            return translations.ca[keys[0]]?.[keys[1]] || key; // fallback to Catalan
        }
    }
    return value;
}

function updateLanguageButton(lang) {
    const langCodes = {
        ca: 'CA',
        es: 'ES',
        en: 'EN',
        ar: 'AR',
        ur: 'UR'
    };
    
    const codeElement = document.querySelector('.current-lang-code');
    
    if (codeElement) codeElement.textContent = langCodes[lang] || 'CA';
}

function updateLanguageDropdown(lang) {
    document.querySelectorAll('.language-dropdown li').forEach(li => {
        const liLang = li.getAttribute('data-lang');
        if (liLang === lang) {
            li.classList.add('active');
        } else {
            li.classList.remove('active');
        }
    });
}

